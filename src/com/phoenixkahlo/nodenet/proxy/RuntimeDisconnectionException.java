package com.phoenixkahlo.nodenet.proxy;

public class RuntimeDisconnectionException extends RuntimeException {

	public RuntimeDisconnectionException() {
		super();
	}

	public RuntimeDisconnectionException(String arg0) {
		super(arg0);
	}

	public RuntimeDisconnectionException(Throwable arg0) {
		super(arg0);
	}

	private static final long serialVersionUID = 3492967446885152139L;

}
