package com.phoenixkahlo.nodenet.proxy;

public class RuntimeProxyException extends RuntimeException {

	private static final long serialVersionUID = 1521773319340836634L;

	public RuntimeProxyException() {
		super();
	}

	public RuntimeProxyException(String message) {
		super(message);
	}

	public RuntimeProxyException(Throwable cause) {
		super(cause);
	}

}
